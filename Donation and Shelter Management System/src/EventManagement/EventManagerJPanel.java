    private void AddEventBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddEventBtnActionPerformed
        
        EventType eventType = EventType.valueOf(String.valueOf(eventTypeBox.getSelectedItem()));
        String name = eventName.getText();
        String date = evntDateTime.getText();
        
        if(eventManagementOrganisation.getEventInEventMangDirectory() == null){
            eventManagementOrganisation.setEventInEventMangDirectory(new EventInEventMangDirectory());
        }
        
        EventInEventManagement eventInEventManagement = eventManagementOrganisation.getEventInEventMangDirectory().addEventInEventManagement();
        
        eventInEventManagement.setDateTime(date);
        eventInEventManagement.setEventType(eventType);
        eventInEventManagement.setName(name);
        
        populateEventsTable();
        
        eventName.setText("");
        evntDateTime.setText("");
        
        
        
    }//GEN-LAST:event_AddEventBtnActionPerformed

    private void deleteEvntBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteEvntBtnActionPerformed
        int selectedRow = eventsTbl.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }
        EventInEventManagement eventInEventManagement = (EventInEventManagement) eventsTbl.getValueAt(selectedRow, 0);
        eventManagementOrganisation.getEventInEventMangDirectory().getEventInEventManagements().remove(eventInEventManagement);
        populateEventsTable();
        

    }//GEN-LAST:event_deleteEvntBtnActionPerformed


