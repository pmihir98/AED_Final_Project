    private void populateEventsTable() {

        DefaultTableModel model = (DefaultTableModel) eventsTbl.getModel();
        model.setRowCount(0);

        if (eventManagementOrganisation.getEventInEventMangDirectory() == null || 
                eventManagementOrganisation.getEventInEventMangDirectory().getEventInEventManagements() == null) {
            JOptionPane.showMessageDialog(this, "No events are scheduled !!!");
            return;
        }

        for (EventInEventManagement e : eventManagementOrganisation.getEventInEventMangDirectory().getEventInEventManagements()) {
            Object[] row = new Object[3];
            row[0] = e;
            row[1] = e.getDateTime();
            row[2] = e.getEventType().getValue();
            model.addRow(row);
        }
    }
