    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        int selectedRow = fundManageTbl.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }

        DonationAddWorkRequest request = (DonationAddWorkRequest) fundManageTbl.getValueAt(selectedRow, 0);

        if (request.getStatus().equalsIgnoreCase("Received")) {
            donationsOrganisation.setAvailableFund(donationsOrganisation.getAvailableFund() - ((Donar) request.getMessage()).getAmount() < 0 ? 0
                    : donationsOrganisation.getAvailableFund() - ((Donar) request.getMessage()).getAmount());
        }
        donationsOrganisation.getWorkQueue().getWorkRequestList().remove(request);
        populateTable();


    }//GEN-LAST:event_deleteBtnActionPerformed

