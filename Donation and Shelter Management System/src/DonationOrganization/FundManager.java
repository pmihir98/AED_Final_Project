    //Popukate only those having donar as message object
    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) fundManageTbl.getModel();

        model.setRowCount(0);

        for (WorkRequest request : donationsOrganisation.getWorkQueue().getWorkRequestList()) {
            if (request instanceof DonationAddWorkRequest && request.getMessage() instanceof Donar){
            
                Donar d = (Donar) request.getMessage();
                Object[] row = new Object[6];
                row[0] = request;
                row[1] = d.getAmount();
                row[2] = d.getName();
                row[3] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
                row[4] = request.getStatus();

                model.addRow(row);
            }

        }
        totalFundsTxt.setText(String.valueOf(donationsOrganisation.getAvailableFund()));
    }
