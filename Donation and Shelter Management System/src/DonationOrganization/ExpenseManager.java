    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        int selectedRow = expTbl.getSelectedRow();
        
        if (selectedRow < 0) {
            return;
        }
        ExpenseManagerWorkRequest request = (ExpenseManagerWorkRequest) expTbl.getValueAt(selectedRow, 0);
        if (request.getStatus().equalsIgnoreCase("Sent")) {
            request.setStatus("DisApproved");
            //donationsOrganisation.setTotalExpenditure(donationsOrganisation.getTotalExpenditure() + Integer.valueOf(request.getMessage().getAmount()));
            //donationsOrganisation.setAvailableFund(donationsOrganisation.getAvailableFund() - Integer.valueOf(request.getMessage().getAmount()));
        } else if (request.getStatus().equalsIgnoreCase("Approved") || request.getStatus().equalsIgnoreCase("Disapproved")) {
            JOptionPane.showMessageDialog(this, "Action already been taken on the selected");
        }
        
        populateExpRequests();
        
    }//GEN-LAST:event_jButton6ActionPerformed

    private void approvBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approvBtnActionPerformed
        int selectedRow = expTbl.getSelectedRow();
        
        if (selectedRow < 0) {
            return;
        }
        ExpenseManagerWorkRequest request = (ExpenseManagerWorkRequest) expTbl.getValueAt(selectedRow, 0);
        if (request.getStatus().equalsIgnoreCase("Sent")) {
            request.setStatus("Approved");
            donationsOrganisation.setTotalExpenditure(donationsOrganisation.getTotalExpenditure() + Integer.valueOf(request.getMessage().getAmount()));
            donationsOrganisation.setAvailableFund(donationsOrganisation.getAvailableFund() - Integer.valueOf(request.getMessage().getAmount()));
        } else if (request.getStatus().equalsIgnoreCase("Approved") || request.getStatus().equalsIgnoreCase("Disapproved")) {
            JOptionPane.showMessageDialog(this, "Action already been taken on the selected");
        }
        populateExpRequests();

    }//GEN-LAST:event_approvBtnActionPerformed

