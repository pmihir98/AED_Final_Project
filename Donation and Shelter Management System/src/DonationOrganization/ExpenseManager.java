    private void populateExpRequests() {
        
        DefaultTableModel model = (DefaultTableModel) expTbl.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest request : donationsOrganisation.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ExpenseManagerWorkRequest && request.getMessage() instanceof ExpenseObject) {
                
                ExpenseObject expObj = (ExpenseObject) request.getMessage();
                Object[] row = new Object[6];
                row[0] = request;
                row[1] = expObj.getPurpose();
                row[2] = expObj.getAmount();
                row[3] = request.getStatus();
                
                model.addRow(row);
            }
            
        }
        
    }
